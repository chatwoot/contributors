// This gets called on every request
export async function getServerSideProps() {
  // Fetch data from external API
  const result = await request("GET /repos/{owner}/{repo}/stats/contributors", {
    headers: {
      authorization: "token GITHUB_AUTH_TOKEN_WITH_ORG_READ_PERMISSION",
    },
    owner: "chatwoot",
    repo: "chatwoot",
  });

  // Pass data to the page via props
  return {
    props: { contributors: result.data.sort((a, b) => b.total - a.total) },
  };
}

